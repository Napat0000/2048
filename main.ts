function NewBlock () {
    while (true) {
        Gamount = 0
        for (let index = 0; index <= Gwidth * Gheight - 1; index++) {
            if (Math.abs(Grid[index]) > 0) {
                Gamount += 1
            } else if (Grid[index] == 0) {
                if (Math.percentChance(1)) {
                    Grid[index] = randint(1, 2)
                    return
                }
            }
        }
        if (Gamount >= Grid.length) {
            return
        }
    }
}
