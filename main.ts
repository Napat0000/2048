function MoveToMerge (Dx: number, Dy: number) {
    for (let index = 0; index <= Gwidth * Gheight - 1; index++) {
        TowardI = ScanInDirection(Dx, Dy, index)
        if (Grid[TowardI] > 0) {
            I = TowardI
            Tile = Grid[TowardI]
            Grid[TowardI] = 0
            while (Grid[I] == 0) {
                I += Dx
                I += Dy * Gwidth
                if (Grid[I] == 0) {
                    Grid[TowardI] = 0
                }
            }
            if (Grid[I] < 0 || Grid[I] != Tile) {
                I += 0 - Dx
                I += 0 - Dy * Gwidth
            }
            if (Grid[I] <= 0) {
                Grid[I] = Tile
            } else if (Grid[I] == Tile) {
                Grid[I] = Tile + 1
            }
        }
    }
}
