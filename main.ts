function ScanInDirection (Dx: number, Dy: number, Idx: number) {
    if (Dy > 0) {
        TowardI = Gwidth * Gheight - 1 - Idx
    } else if (Dy < 0) {
        TowardI = Idx
    } else if (Dx < 0) {
        TowardI = Idx
        Gx = Math.floor(TowardI / Gheight)
        Gy = TowardI % Gheight
        TowardI = Gx
        TowardI += Gy * Gheight
    } else if (Dx > 0) {
        TowardI = Gwidth * Gheight - 1 - Idx
        Gx = Math.floor(TowardI / Gheight)
        Gy = TowardI % Gheight
        TowardI = Gx
        TowardI += Gy * Gheight
    }
    return TowardI
}
function WriteModuleGrid () {
    WriteNumber(0.1)
    WriteNumber(OffsetX)
    WriteNumber(OffsetY)
    WriteNumber(Gwidth)
    WriteNumber(Gheight)
    Gidx = 0
    Tile = Grid[Gidx]
    Count = 1
    for (let index = 0; index < Grid.length - 1; index++) {
        Gidx += 1
        if (Tile == Grid[Gidx]) {
            Count += 1
        } else {
            WriteNumber(Count)
            WriteNumber(Tile)
            Tile = Grid[Gidx]
            Count = 1
        }
    }
    WriteNumber(Count)
    WriteNumber(Tile)
    WriteNumber(-2048)
}
